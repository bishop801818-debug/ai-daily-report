def generate_html_content(division_name: str, news_list: list) -> str:
    """生成事业部早报 HTML 内容"""
    html_parts = []
    
    # 头条聚焦
    if news_list:
        top_title, top_content, top_impact = news_list[0]
        html_parts.append('<div class="report-section">')
        html_parts.append('  <div class="report-section-title">📰 头条聚焦</div>')
        html_parts.append('  <div class="report-item">')
        html_parts.append(f'    <div class="item-title">{top_title}</div>')
        html_parts.append(f'    <div class="item-content">{top_content}</div>')
        html_parts.append(f'    <div class="item-meta"><span class="impact">💡 影响：{top_impact}</span></div>')
        html_parts.append('  </div>')
        html_parts.append('</div>')
    
    # 市场动态
    market_news = news_list[1:3] if len(news_list) > 1 else []
    if market_news:
        html_parts.append('<div class="report-section">')
        html_parts.append('  <div class="report-section-title">📈 市场动态</div>')
        for title, content, impact in market_news:
            html_parts.append('  <div class="report-item">')
            html_parts.append(f'    <div class="item-title">{title}</div>')
            html_parts.append(f'    <div class="item-content">{content}</div>')
            html_parts.append(f'    <div class="item-meta"><span class="impact">💡 影响：{impact}</span></div>')
            html_parts.append('  </div>')
        html_parts.append('</div>')
    
    # 行业前沿
    if len(news_list) > 3:
        tech_news = news_list[3:]
        html_parts.append('<div class="report-section">')
        html_parts.append('  <div class="report-section-title">🔬 行业前沿</div>')
        for title, content, impact in tech_news[:3]:
            html_parts.append('  <div class="report-item">')
            html_parts.append(f'    <div class="item-title">{title}</div>')
            html_parts.append(f'    <div class="item-content">{content}</div>')
            html_parts.append(f'    <div class="item-meta"><span class="impact">💡 影响：{impact}</span></div>')
            html_parts.append('  </div>')
        html_parts.append('</div>')
    
    return '\n'.join(html_parts)


def main():
    """生成早报 JSON"""
    report_date = "2026-04-09"
    
    departments = {}
    
    for division_name, news_list in DIVISION_NEWS.items():
        html_content = generate_html_content(division_name, news_list)
        division_id = DIVISION_ID_MAP.get(division_name, division_name.lower())
        
        departments[division_id] = {
            "name": division_name,
            "_raw_content": html_content,
            "news_count": len(news_list),
            "last_updated": f"{report_date} 09:00"
        }
    
    # 构建 JSON 结构
    output_data = {
        "report_date": report_date,
        "window": "2026-04-07 至 2026-04-09",
        "generated_at": f"{report_date} 09:00",
        "source": "实时搜索数据（妙想搜索 + 联网搜索）",
        "departments": departments
    }
    
    # 保存 JSON
    output_file = "reports/2026-04-09_realtime.json"
    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(output_data, f, ensure_ascii=False, indent=2)
    
    print(f"[OK] 实时搜索版早报已生成：{output_file}")
    print(f"[OK] 共 {len(departments)} 个事业部")
    
    # 统计各事业部新闻数量
    for div_name, div_data in departments.items():
        print(f"  - {div_data['name']}: {div_data['news_count']} 条")


if __name__ == "__main__":
    main()

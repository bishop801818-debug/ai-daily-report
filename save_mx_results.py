# ============================================================
# 整理为统一格式（title作为content的摘要，补充content字段）
# ============================================================
def make_content(title: str, source: str) -> str:
    """从标题+来源构造有信息量的 content 摘要"""
    return f"来源：{source}。{title}"

output = []
seen_urls = set()

for bu_data in raw_results:
    for item in bu_data["results"]:
        title = item.get("title", "")
        source = item.get("source", "")
        url = item.get("url", "")
        date = item.get("date", "")

        if not title:
            continue

        # 去重（以title作为去重标识，因为大多数没有url）
        uid = title
        if uid in seen_urls:
            continue
        seen_urls.add(uid)

        # content = 标题本身作为摘要（mx_search返回的即标题+来源）
        content = make_content(title, source)

        output.append({
            "title": title,
            "content": content,
            "url": url,
            "date": date,
            "source": source,
        })

print(f"共整理 {len(output)} 条结果（去重后）")

# 保存
out_path = "D:/trae/AI Daily report/search_results.json"
with open(out_path, "w", encoding="utf-8") as f:
    json.dump(output, f, ensure_ascii=False, indent=2)

print(f"已保存到: {out_path}")